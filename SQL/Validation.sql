-- 6. DUPLICATE CHECKS

SELECT TRANSACTION_ID, COUNT(*) AS RECORD_COUNT
FROM STAGING.STG_SALES
GROUP BY TRANSACTION_ID
HAVING COUNT(*) > 1
ORDER BY RECORD_COUNT DESC;


SELECT PRODUCT_ID, COUNT(*) AS RECORD_COUNT
FROM MART.DIM_PRODUCT
GROUP BY PRODUCT_ID
HAVING COUNT(*) > 1
ORDER BY RECORD_COUNT DESC;


SELECT STORE_ID, COUNT(*) AS RECORD_COUNT
FROM MART.DIM_STORE
GROUP BY STORE_ID
HAVING COUNT(*) > 1
ORDER BY RECORD_COUNT DESC;



-- 7. ORPHAN KEY CHECKS IN FACT TABLES

SELECT COUNT(*) AS INVALID_PRODUCT_KEYS_IN_FACT_SALES
FROM MART.FACT_SALES f
LEFT JOIN MART.DIM_PRODUCT p
    ON f.PRODUCT_ID = p.PRODUCT_ID
WHERE p.PRODUCT_ID IS NULL;


SELECT COUNT(*) AS INVALID_STORE_KEYS_IN_FACT_SALES
FROM MART.FACT_SALES f
LEFT JOIN MART.DIM_STORE s
    ON f.STORE_ID = s.STORE_ID
WHERE s.STORE_ID IS NULL;


SELECT COUNT(*) AS INVALID_PRODUCT_KEYS_IN_FACT_INVENTORY
FROM MART.FACT_INVENTORY_DAILY i
LEFT JOIN MART.DIM_PRODUCT p
    ON i.PRODUCT_ID = p.PRODUCT_ID
WHERE p.PRODUCT_ID IS NULL;


SELECT COUNT(*) AS INVALID_STORE_KEYS_IN_FACT_INVENTORY
FROM MART.FACT_INVENTORY_DAILY i
LEFT JOIN MART.DIM_STORE s
    ON i.STORE_ID = s.STORE_ID
WHERE s.STORE_ID IS NULL;


