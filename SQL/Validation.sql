-- 10. DIM_DATE VALIDATION


SELECT MIN(DATE_DAY) AS MIN_DATE,
       MAX(DATE_DAY) AS MAX_DATE,
       COUNT(*) AS TOTAL_DATES
FROM MART.DIM_DATE;


SELECT COUNT(*) AS MISSING_SALES_DATES_IN_DIM_DATE
FROM (
    SELECT DISTINCT TRANSACTION_DATE AS DATE_DAY
    FROM MART.FACT_SALES
) s
LEFT JOIN MART.DIM_DATE d
    ON s.DATE_DAY = d.DATE_DAY
WHERE d.DATE_DAY IS NULL;


SELECT COUNT(*) AS MISSING_INVENTORY_DATES_IN_DIM_DATE
FROM (
    SELECT DISTINCT SNAPSHOT_DATE AS DATE_DAY
    FROM MART.FACT_INVENTORY_DAILY
) i
LEFT JOIN MART.DIM_DATE d
    ON i.DATE_DAY = d.DATE_DAY
WHERE d.DATE_DAY IS NULL;



